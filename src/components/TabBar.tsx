const tabs = [
  { path: '/', label: '首页', Icon: IconHome },
  { path: '/calendar', label: '日历', Icon: IconCalendar },
  { path: '/stats', label: '统计', Icon: IconStats },
  { path: '/family', label: '家庭', Icon: IconFamily },
];

export default function TabBar() {
  const location = useLocation();
  const navigate = useNavigate();

  // Hide tab bar on form pages
  if (location.pathname.startsWith('/course/') || location.pathname.startsWith('/history/')) return null;

  return (
    <div className="fixed bottom-0 left-1/2 -translate-x-1/2 w-full max-w-[480px] bg-white border-t border-gray-100 flex z-50">
      {tabs.map(tab => {
        const active = tab.path === '/' ? location.pathname === '/' : location.pathname.startsWith(tab.path);
        return (
          <button
            key={tab.path}
            onClick={() => navigate(tab.path)}
            className={`flex-1 flex flex-col items-center py-2 text-xs transition-colors ${
              active ? 'text-pink-500' : 'text-gray-400'
            }`}
          >
            <tab.Icon active={active} />
            <span className={`mt-0.5 ${active ? 'font-semibold' : ''}`}>{tab.label}</span>
          </button>
        );
      })}
    </div>
  );
}
